void solve()
{
    ll m, n, k;
    cin >> m;
    
    if (m == 2)
    {
        n = 4;
        k = 8;
    }
    else
    {
        n = 2 * m;
        k = m * m;
    }

    cout << n << " " << k << endl;
}
int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int t;
    cin >> t;
    while (t--)
    {
        solve();
    }
    return 0;
}